package alberapps.java.directions;

import java.util.List;

/**
 * Datos leg
 */
public class Leg {

    private String distance;

    private String duration;

    private String arrivalTime;

    private String departureTime;

    private String endAdress;

    private String startArddress;

    private List<String> resumen;

    private String startLat;

    private String endLat;

    private String startLng;

    private String endLng;

    public String getEndLat() {
        return endLat;
    }

    public void setEndLat(String endLat) {
        this.endLat = endLat;
    }

    public String getEndLng() {
        return endLng;
    }

    public void setEndLng(String endLng) {
        this.endLng = endLng;
    }

    public String getStartLat() {
        return startLat;
    }

    public void setStartLat(String startLat) {
        this.startLat = startLat;
    }

    public String getStartLng() {
        return startLng;
    }

    public void setStartLng(String startLng) {
        this.startLng = startLng;
    }

    public List<String> getResumen() {
        return resumen;
    }

    public void setResumen(List<String> resumen) {
        this.resumen = resumen;
    }

    private List<Step> steps;

    public List<Step> getSteps() {
        return steps;
    }

    public void setSteps(List<Step> steps) {
        this.steps = steps;
    }

    public String getArrivalTime() {
        return arrivalTime;
    }

    public void setArrivalTime(String arrivalTime) {
        this.arrivalTime = arrivalTime;
    }

    public String getDepartureTime() {
        return departureTime;
    }

    public void setDepartureTime(String departureTime) {
        this.departureTime = departureTime;
    }

    public String getDistance() {
        return distance;
    }

    public void setDistance(String distance) {
        this.distance = distance;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getEndAdress() {
        return endAdress;
    }

    public void setEndAdress(String endAdress) {
        this.endAdress = endAdress;
    }

    public String getStartArddress() {
        return startArddress;
    }

    public void setStartArddress(String startArddress) {
        this.startArddress = startArddress;
    }
}
